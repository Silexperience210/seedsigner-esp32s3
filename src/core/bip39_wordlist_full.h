// Inline helper - copies word to buffer
inline void get_word_copy(uint16_t index, char* buffer, size_t buf_len) {
    if (index >= WORDLIST_SIZE || buffer == nullptr || buf_len == 0) return;
    // Simple linear search for now (can be optimized with offset table)
    uint16_t count = 0;
    uint16_t pos = 0;
    while (count < index) {
        if (pgm_read_byte(&WORDLIST_BYTES[pos]) == 0) count++;
        pos++;
    }
    // Copy word
    uint8_t i = 0;
    uint8_t c;
    while ((c = pgm_read_byte(&WORDLIST_BYTES[pos + i])) != 0 && i < buf_len - 1) {
        buffer[i] = c;
        i++;
    }
    buffer[i] = '\0';
}

